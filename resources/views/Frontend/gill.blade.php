                                    West Green School, Haringey, and her facility with the English language and literature and her considerable ability to play the piano and sing was there for all to see, hear and admire. She was soon on the permanent
                                    staff, responsible for English, drama and music throughout the school. Though most of her adult life was spent in England, Gill identified strongly with her Welsh heritage.
